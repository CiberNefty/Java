
public class Ejercicio11 {

	public static void main(String[] args) {
		// Bucle While
		int i = 16; //Iterador
		
		while (i < 10) {
			System.out.print("El valor es: " + i +"\n");
			i++; //Incrementamos
		}
		
		// Bucle Do-While
		int e=1;
		do {
			System.out.println("El valor de e es: "+e);
			e++;
		} while (e < 10);
	}

}
